'''
Escreva um programa em Python que leia um número e represente
o número antecessor e sucessor desse número que foi lido,
utilizando operadores de atribuição.
'''

numDigitado = int(input("Digite um numero \n"))

antecessor = numDigitado +-1
sucessor = numDigitado ++1

print(f"## Antecessor de {numDigitado} é {antecessor} e seu Sucessor é {sucessor} ##")
